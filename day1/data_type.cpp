#include <iostream>
using namespace std;
int main() {
    int a;
    cout<<"Size of int:\n"<<sizeof(a)<<endl; 
    float b;
    cout<<"Size of float:\n"<<sizeof(b)<<endl; 
    char c;
    cout<<"Size of char:\n"<<sizeof(c)<<endl; 
    bool d;
    cout<<"Size of bool:\n"<<sizeof(d)<<endl; 
    return 0;
}